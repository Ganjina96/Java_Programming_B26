package day23_loops;

public class ForLoopExample {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }



        /*
         flow of for loop
            1) it executes the initialization part to declare and assign a variable
            initialization: int i = 1;
            initialization part runs once in the beginning
            2) the boolean/termination condition is checked, to see when it will be stopped
            -> if the  boolean is true then the statement is executed
            -> if the boolean is false then the loop will stop
            in the above example: i <= 10
            3) statements in the code body are executed top to bottom
            -> in our ex: System.out.println(i);
            4) update happens -- this is the end of an iteration
            -> in our ex i++;
            5) goes back to step 2 and repeats
         */









    }
}
